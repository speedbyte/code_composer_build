/*-----------------------------------------------------------------------------+
|    F U N C T I O N   I N F O R M A T I O N                                   |
+------------------------------------------------------------------------------+
|                                                                              |
|  Function Name:  FUN_CCglo_GetRandomValue_UL                                 |
|                                                                              |
|  Prototype at:   ADLATUS_Global_TDF.H                                        |
|                                                                              |
|  Description:    This function creats a random value.                        |
|                                                                              |
|  Parameter:      -                                                           |
|                                                                              |
|  Return Value:   ULONG t_RandomValue_UL  .. random value                     |
|                                                                              |
+-----------------------------------------------------------------------------*/
ULONG FUN_CCglo_GetRandomValue_UL (void)
{
  /*--------------------------------------------------------------------------*/
  /* 4 Byte Scramble variable for random value creation                       */
  /*--------------------------------------------------------------------------*/
  union
  {
    struct
    {
      UBYTE Byte0_UB;   /*  1 Byte - Byte 00 */
      UBYTE Byte1_UB;   /*  1 Byte - Byte 01 */
      UBYTE Byte2_UB;   /*  1 Byte - Byte 02 */
      UBYTE Byte3_UB;   /*  1 Byte - Byte 03 */
    } Byte_ST;
    struct
    {
      UWORD Word0_UW;   /*  2 Byte - Word 00 */
      UWORD Word1_UW;   /*  2 Byte - Word 01 */
    } Word_ST;
    struct
    {
      ULONG Long0_UL;   /*  4 Byte - Long 00 */
    } Long_ST;
  } t_RandomValue_U4;
  /*- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - */

  /*--------------------------------------------------------------------------*/
  /* 2 Byte Hardware timer value                                              */
  /*--------------------------------------------------------------------------*/
  union
  {
    struct
    {
      UBYTE Byte0_UB;   /*  1 Byte - Byte 00 */
      UBYTE Byte1_UB;   /*  1 Byte - Byte 01 */
    } Byte_ST;
    struct
    {
      UWORD Word0_UW;   /*  2 Byte - Word 00 */
    } Word_ST;
  } t_HWTimerValue_U2;
  
  /*- - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - */

  t_HWTimerValue_U2.Word_ST.Word0_UW = FUN_CChal_GetHWTimerValue_UW();

  t_RandomValue_U4.Word_ST.Word0_UW = (t_HWTimerValue_U2.Word_ST.Word0_UW >> 0x03);
  t_RandomValue_U4.Word_ST.Word1_UW = (t_HWTimerValue_U2.Word_ST.Word0_UW >> 0x05);
  t_RandomValue_U4.Byte_ST.Byte1_UB = (t_HWTimerValue_U2.Byte_ST.Byte0_UB >> 0x01);
  t_RandomValue_U4.Byte_ST.Byte3_UB = (t_HWTimerValue_U2.Byte_ST.Byte0_UB        );


  return ( t_RandomValue_U4.Long_ST.Long0_UL );


} /*-+- END OF FUNCTION 'FUN_CCglo_GetRandomValue_UL' -+-+-+-+-+-+-+-+-+-+-+-+*/


