/*----------------------------------------------------------------------------*/
/* Function    : InitIdleTask                                                 */
/** 
    \brief      Callback function for initialization of OsIdleTask_bg
  
    \brief      Description:
				Callback function to initialize the OsIdleTask_bg this
				function will be only called one time during first activation 
				of OsIdleTask_bg 

    \return     SuccessFailType
    			Status of function run
    			SUCCESS, FAIL
   */
/*----------------------------------------------------------------------------*/
SuccessFailType InitIdleTask(void)
{
    SuccessFailType sF = SUCCESS;
    
    //Set hardware Pin for performance measurement
    (void)SPI_vPinModify(SPI5, SpiSomiPin, PortIoOutHigh);

	(void)SetRelAlarm(CTA_WD, MSEC(2), MSEC(2));
    return(sF);
}

/*----------------------------------------------------------------------------*/
/* Function    : IdleTask_bg                                                  */
/** 
    \brief      Callback function for OsIdleTask_bg
  
    \brief      Description:
                Callback function from the OsIdleTask_bg this function will 
				be run in the background and supported the watchdog and the 
				runtime debugging 

    \return     void
   */
/*----------------------------------------------------------------------------*/
void IdleTask_bg(void)
{
    EventMaskType events;
	
    (void)GetEvent(OsIdleTask_bg, &events);
    
    if (events & evTriggerWD)
    {
        (void)WD_u8ToggleWD();
        (void)ClearEvent(evTriggerWD);
    }

    return;
}



