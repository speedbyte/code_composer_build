SuccessFailType VCR_sFChangeLanguageViaCan(APP_CAN_enLang newCANLangID)
{
    SuccessFailType sF = SUCCESS;

    if ((_VCR_gData.bTrigLangChange == BT_TRUE) || (_VCR_gData.eSystemState == VCR_SYSTEM_STARTED_LANG))
    {
       // RT#5540 UHV High: Sprachbed. folgt nicht Wechsel Sprache
       // a language change is ongoing or ready but not spoken that it is done
       // store only the new CAN-Language, start later
       _VCR_gData.QueuedCANLangID = newCANLangID;
       _VCR_bLangChangeQueued = BT_TRUE;

       DEBUG_VALUE1(VCR_SWI_TRC, MOD_VCR, "VR Language change is ongoing - change later with CAN-Lang %d", _VCR_gData.QueuedCANLangID);

       sF = FAIL;
    }
    else
    {
       // no Language change is ongoing, start now
       DEBUG_VALUE1(VCR_SWI_TRC, MOD_VCR, "No VR Language change is ongoing - change now with CAN-Lang %d", newCANLangID);

       VCR_sFChangeLanguageExecute(newCANLangID);
    }

    return(sF);
}


