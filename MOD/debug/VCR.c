/* Function    : VCR_vCBReadSMS                                               */
/**
    \brief      callback for SMS application to read the SMS content for dialling

    \brief

    \param

    \return     none
   */
/*----------------------------------------------------------------------------*/
void VCR_vCBReadSMS(APP_COMMON_enResponseCode response)
{   /* only call response handler */
    const AppSmsReadSmsType * p_info = NULL;
    switch (response)
    {
        case  APP_COMMON_RSP_OK:
        {   /* the related SMS content has been fetched from AT - get it */
            p_info = APP_SMS_pGetPmrdContext(0); /* we only ask for one element at index 0*/
            /* check that data is valid */
            if (BT_TRUE == p_info->isValid)
            {
                if (0 < p_info->data.origin_len)
                {   /* store the origin information */
                    _VCR_gData.resultInfo.readSMS.origin_len = p_info->data.origin_len;
                    (void)memcpy(&(_VCR_gData.resultInfo.readSMS.origin),
                           &(p_info->data.origin) ,p_info->data.origin_len);
                }
                if (0 < p_info->data.message_len)
                {   /* store the sms message */
                    _VCR_gData.resultInfo.readSMS.message_len = p_info->data.message_len;
                    if (AT_MAX_SMS_LEN_TOTAL < _VCR_gData.resultInfo.readSMS.message_len)
                    {
                        _VCR_gData.resultInfo.readSMS.message_len = AT_MAX_SMS_LEN_TOTAL;
                    }
                    (void)memcpy(&(_VCR_gData.resultInfo.readSMS.message),
                           &(p_info->data.message) ,
                           _VCR_gData.resultInfo.readSMS.message_len);
                }

                 DEBUG_VALUE2(VCR_SWI_TRC, MOD_VCR, "VCR_vCBReadSMS CB - SMS ori_len %d mes_len %d",_VCR_gData.resultInfo.readSMS.origin_len,_VCR_gData.resultInfo.readSMS.message_len);

                _VCR_vSendTriggerToMainEventHandler(VCR_MEVT_SMS_INFO_AVAIL);
            }
            else
            {
                _VCR_PlayError(__LINE__);
            }
            break;
        }
        case  APP_COMMON_RSP_ERROR_BUSY:
        case  APP_COMMON_RSP_ERROR:
        case  APP_COMMON_RSP_ERROR_BAP_PARSE:
        case  APP_COMMON_RSP_ERROR_UART_BLOCKED:
        case  APP_COMMON_RSP_ERROR_AT_SEND:
        case  APP_COMMON_RSP_ERROR_AT_BUFFER_TOO_SHORT:
        case  APP_COMMON_RSP_ERROR_NOT_INITIALIZED:
        default:
        {   /* an error occured, error output and stop system */
            _VCR_PlayError(__LINE__);
            break;
        }
    }
}

