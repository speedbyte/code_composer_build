/* Function    : VCR_sFHapticTrigger                                          */
/**
    \brief      function to trigger haptics towards SIVR

    \brief      if the SIVR is IDLE, a valid trigger will activate SIVR\n
                if SIVR is active all haptic triggers will be rejected and\
                function will return FAIL

    \param      VCRHapticTriggerEnum haptic_type - indicate the specific trigger

    \param      Uint16Type  index needed for most trigger trypes\n
                only VCR_HAPTIC_DELETE_ALL and VCR_HPATIC_PLAY_VMEMO just\n
                ignore the index

    \return     SUCCESS - triggered the specific SIVR action
                FAIL - invalid SIVR state or SIVR not available or invalid type
   */
/*----------------------------------------------------------------------------*/
SuccessFailType VCR_sFHapticTrigger(VCRHapticTriggerEnum haptic_type,
                                    Uint16Type           index)
{
    SuccessFailType sF = SUCCESS;

    /* we also need to check the current call state */
    if ( (VCR_SYSTEM_AVAILABLE == _VCR_gData.eSystemState) &&
         (BT_TRUE == APP_PHONE_AT_PNCC_bAllCallStatesIdle()) &&
         (ERM_SIVR_NOT_SUPPORTED != _VCR_gData.eSIVRSupported) &&           /* RT 2429 */
         (BT_FALSE == _VCR_gData.bSivrSyncStarted))
    {   /* check received type */
        switch (haptic_type)
        {
            case VCR_HAPTIC_PLAY_NAME:
            case VCR_HAPTIC_STORE_NAME:
            case VCR_HAPTIC_DELETE_NAME:
            case VCR_HAPTIC_READ_NEW_SMS:
            case VCR_HAPTIC_READ_SMS:
            case VCR_HAPTIC_PLAY_VMEMO:
            {   /* store type and index */
                _VCR_gData.eHapticTrigType = haptic_type;
                _VCR_gData.u16HapticIdx    = index;
                /* trigger machine immediately */
                sF = _VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_HAPTIC,
                        AT_SIVR_CMD_UNKNOWN);
                break;
            }
            case VCR_HAPTIC_NONE:
            default:
            {

                DEBUG_VALUE2(VCR_SWI_WNG, MOD_VCR, "Haptic type %d invalid %d", haptic_type,_VCR_gData.eSIVRSupported);

                sF = FAIL;
                break;
            }
        }
    }
    else
    {

        DEBUG_VALUE2(VCR_SWI_WNG, MOD_VCR, "Haptic Trigger in invalid VR SystemState %d or sync started %d", _VCR_gData.eSystemState,_VCR_gData.bSivrSyncStarted);
        sF = FAIL;
    }
    return sF;
}


