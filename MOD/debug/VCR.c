/* Function    : VCR_sFGetCurrentPhoneNumberforDial                           */
/**
    \brief      function to access current phone number for dial number
                in case needed by SD

    \brief

    \param      pointer to ATPhoneNumberType for phone number

    \param      pointer to Uint16Type for phone number length

    \return     SuccessFailType SUCCESS if correct menu state and type
                                FAIL otherwise
   */
/*----------------------------------------------------------------------------*/
SuccessFailType VCR_sFGetCurrentPhoneNumberforDial(
        ATPhoneNumberType * p_number,
        Uint16Type        * p_number_len)
{
    SuccessFailType sF = FAIL;

    if ( (VCR_RESULT_TYPE_NUMBER  == _VCR_gData.resultInfo.number.type) &&
         (BT_FALSE == _VCR_gData.resultInfo.number.is_for_contact)  &&
         (AT_MAX_PHONE_NUM_LEN >= _VCR_gData.resultInfo.number.number_len ))
    {   /* ensure /0 termination */
        _VCR_gData.resultInfo.number.number[_VCR_gData.resultInfo.number.number_len] = '\0';
        (void)memcpy(p_number,&(_VCR_gData.resultInfo.number.number[0]),
                _VCR_gData.resultInfo.number.number_len);
        *p_number_len = _VCR_gData.resultInfo.number.number_len;
        sF = SUCCESS;
    }
    else if ( (VCR_RESULT_TYPE_NUMBER  == _VCR_gData.resultInfo.number.type) &&
         (BT_FALSE == _VCR_gData.resultInfo.number.is_for_contact)  &&
         (_VCR_gData.resultInfo.number.number_len > AT_MAX_PHONE_NUM_LEN))
    {   /* ensure /0 termination */
        DEBUG_VALUE2(VCR_SWI_WNG, MOD_VCR,"VCR_sFGetCurrentPhoneNumberforDial Cut number_length %d to max %d chars",_VCR_gData.resultInfo.number.number_len, AT_MAX_PHONE_NUM_LEN);

        _VCR_gData.resultInfo.number.number[AT_MAX_PHONE_NUM_LEN] = '\0';
        (void)memcpy(p_number,&(_VCR_gData.resultInfo.number.number[0]),
                AT_MAX_PHONE_NUM_LEN);
        *p_number_len = AT_MAX_PHONE_NUM_LEN;
        sF = SUCCESS;
    }
    return sF;
}

