/* Function    : VCR_vCb3BM_PTT                                               */
/**
    \brief      Callback for Push To Talk

    \brief      this CB is called by 3 button module manager to indicate PTT was
                pressed

    \param      none

    \return     none
   */
/*----------------------------------------------------------------------------*/
void            VCR_vCb3BM_PTT(void)
{
    if ((ERM_SIVR_NOT_SUPPORTED != _VCR_gData.eSIVRSupported) &&          /* RT 2429 */
        (BT_FALSE == _VCR_gData.bSivrSyncStarted))
    {
        if((_VCR_gData.eSystemState == VCR_SYSTEM_FLASHERASE_IPR) ||
           (IGNITION_OFF_DELAY_KL15_OFF_PREPARE_CK5050_SENT == CSHDL_enGetIgnitionOFFDelayState()) ||
           (IGNITION_OFF_DELAY_KL15_OFF_BEFORE_CK5050_OFF_RUNNING == CSHDL_enGetIgnitionOFFDelayState()) )
        {
            _VCR_vNotifyApplication(VCR_NOTIFY_NOT_READY);
        }
        else
        {
            if ((Uint8Type) APP_SD_CONTEXT_S117 == APP_SD_u8GetContextId() &&
                (AT_SIVR_ENG_BEGIN_PROMPT == _VCR_gData.stCtrlInfo.eEngineStatus))
            {
                //RT#4274
                DEBUG_TEXT(VCR_SWI_TRC, MOD_VCR, "CONTEXT S117 (Anrufname) active and VCR speaks -> ignore PTT");
                return;
            }
            else if ((Uint8Type) APP_SD_CONTEXT_S117 == APP_SD_u8GetContextId() &&
                     (AT_SIVR_ENG_BEGIN_PROMPT != _VCR_gData.stCtrlInfo.eEngineStatus))
            {
                _VCR_bPTTPressed = BT_TRUE;

                DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "PTT pressed SIVR supported %d=>$ERMSIVRSupportEnum$",_VCR_gData.eSIVRSupported);
                DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "RSTS:%d=>$ATSIVREngStatusEnum$ => accept PTT",_VCR_gData.stCtrlInfo.eEngineStatus);

                (void)APP_COMMON_UTILS_stSetOsekRelAlarm( CTA_VCR_PENDING,  MSEC(10),   0u);

                APP_SD_vSetVcrIsActive();

                DEBUG_TEXT(VCR_SWI_TRC, MOD_VCR, "CONTEXT S117 (Anrufname) active and VCR");
            }
            else
            {
                _VCR_bPTTPressed = BT_TRUE;

                DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "PTT pressed SIVR supported %d=>$ERMSIVRSupportEnum$",_VCR_gData.eSIVRSupported);
                DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "RSTS:%d=>$ATSIVREngStatusEnum$ => accept PTT",_VCR_gData.stCtrlInfo.eEngineStatus);

                (void)APP_COMMON_UTILS_stSetOsekRelAlarm( CTA_VCR_PENDING,  MSEC(10),   0u);
            }
        }
    }
    else
    {
        // if Sync. is started and the VCR is not coded notify application
        if((ERM_SIVR_NOT_SUPPORTED != _VCR_gData.eSIVRSupported) && (BT_TRUE == _VCR_gData.bSivrSyncStarted))
        {
            _VCR_vNotifyApplication(VCR_NOTIFY_NOT_READY);
        }

        DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "PTT pressed SIVR supported %d=>$ERMSIVRSupportEnum$",_VCR_gData.eSIVRSupported);
        DEBUG_STATE1(VCR_SWI_TRC, MOD_VCR, "RSTS:%d=>$ATSIVREngStatusEnum$ => ignore PTT",_VCR_gData.stCtrlInfo.eEngineStatus);
    }
}


