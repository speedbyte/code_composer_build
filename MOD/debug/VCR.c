/* Function    : VCR_vHandleEvents                                            */
/**
    \brief      Main VCR event handler called within applciation task

    \brief      function where incoming events are processed and VCR internal\n
                state is handled

    \param      events
                EventMaskType
                all supported events from mask type

    \return     none
   */
/*----------------------------------------------------------------------------*/
void VCR_vHandleEvents(EventMaskType events)
{
    // remove event evAppTaskVCRATResponse and message msgVCRATResponse
    // from APP_PHONE_vHandleEvents to central AT-handling in
    // APP_COMMON_vHandleEvents

    if( events & evAppTaskVCRSupervisor )
    {
        (void)ClearEvent( evAppTaskVCRSupervisor );
        /* go back to IDLE if not already in idle */
        if (_VCR_gData.eMenuState != AT_SIVR_MENU_IDLE ||
            (VCR_SYSTEM_AVAILABLE != _VCR_gData.eSystemState &&
            VCR_SYSTEM_NOT_AVAILABLE != _VCR_gData.eSystemState ) )
        {
            DEBUG_VALUE2(VCR_SWI_WNG, MOD_VCR,"VCRSupervisor SystemState %d  MenuState %d",_VCR_gData.eSystemState,_VCR_gData.eMenuState);

            _VCR_vSystemStopToIdle(BT_FALSE);
        }

        /* don't trigger HW Watchdog anymore  */
        ERM_Execute_HW_Reset();
    }

    /* check if the PTT was pressed - if so due to current state different handling
     * required */
    if ( (events & evAppTaskVCRPending) &&
         (VCR_MEVT_UNDEF != _VCR_gData.eEventTrigMain))
    {
        /* call main processing function to handle internal triggers */
        (void)_VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_UNDEF,
                           AT_SIVR_CMD_UNKNOWN);
    }
    else if ((events & evAppTaskVCRPending) &&
        (AT_SIVR_CMD_UNKNOWN != _VCR_gData.eCmdTrigMain) )
    {
        /* call main processing function to handle internal triggers */
        (void)_VCR_sFHandleEvent(BT_FALSE,VCR_MEVT_UNDEF,
                           AT_SIVR_CMD_UNKNOWN);
    }
    else if ((BT_TRUE == _VCR_bPTTPressed) && (ERM_JINGLE_NO_JINGLE_ACTIVE == ERM_GetJingleState()))
    {   /* Note: trigger event transition to system state machine and
           reset PTT pressed */
        if (SUCCESS == _VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_PTT,
                                          AT_SIVR_CMD_UNKNOWN))
        {
            _VCR_bPTTPressed = BT_FALSE;
        }
    }
    else if (BT_TRUE == _VCR_bCALLPressed)
    {   /* Note: trigger event transition to system state machine and
           reset CALL pressed */
        if (SUCCESS == _VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_CALL,
                                          AT_SIVR_CMD_UNKNOWN))
        {
            _VCR_bCALLPressed = BT_FALSE;
        }
    }
    /* check if language change trigger required */
    else if ( (BT_TRUE == _VCR_bLangChanged)  &&
              (AT_AUDIO_STATE_NO_ACTIVE == APP_DEVICE_GetPASAEvent()) &&
              (_VCR_gData.stCtrlInfo.eEngineStatus == AT_SIVR_ENG_IDLE || (_VCR_gData.stCtrlInfo.eEngineStatus == AT_SIVR_ENG_END_SYNC)) &&
              (_VCR_gData.eSystemState == VCR_SYSTEM_AVAILABLE) // RT 4471
            )
    {   /* Note: trigger event transition to system state machine and
           reset language change if processed successfull
           Host-Spec: Language change only allowed if there not a active audio source
         */
        if (SUCCESS == _VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_LANG_CHANG_REG,
                                          AT_SIVR_CMD_UNKNOWN))
        {
            _VCR_bLangChanged = BT_FALSE;
        }
    }
    else if (BT_TRUE == _VCR_bLangChangeQueued)
    {
        if (SUCCESS == VCR_sFChangeLanguageViaCan(_VCR_gData.QueuedCANLangID))
        {
           _VCR_bLangChangeQueued = BT_FALSE;
        }
    }

    /* clear Pending event */
    if( events & evAppTaskVCRPending )
    {
        /* check if a message needs to be send towards CK5050 */
        _VCR_vCheckAndSendATMessage(events);
        (void)ClearEvent( evAppTaskVCRPending );
    }
}

