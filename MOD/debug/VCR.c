/* Function    : VCR_vCBGetPBInfoSingleEntryUserPB                            */
/**
    \brief      callback for APP_xxx if message towards CK5050 is send via APP

    \brief      process ok or ERROR from ERM and if required trigger state changes

    \param      BooleanType isOK - BT_TRUE if event was OK, ERROR otherwise

    \return     none
   */
/*----------------------------------------------------------------------------*/
void VCR_vCBGetPBInfoSingleEntryUserPB(APP_COMMON_enResponseCode response)
{
    Uint32Type i = 0u;
    const AppPbEntryType * p_pb_entry = NULL;

    switch (response)
    {
        case APP_COMMON_RSP_OK:
        {   /* query the required information from APP PB */
            p_pb_entry = APP_PHONEBOOK_pGetPpbrContext(0);
            if (NULL != p_pb_entry)
            {   /* check which is the current used type */
                VCRResultContactUniType * p_uni = &(_VCR_gData.resultInfo.contact_uni);
                /* check entry is valid*/
                if (BT_TRUE == p_pb_entry->isValid)
                {   /* copy data */
                    p_uni->n = p_pb_entry->ppbr.pn_size;
                    if (1 == p_uni->n &&
                        AT_TRUE == p_pb_entry->ppbr.pn_type_numer_available)
                    {   /* we need to match the location */
                        /* p_uni->location_avail = BT_TRUE; */
                        p_uni->c = 1u;
                        switch (p_pb_entry->ppbr.pn_type)
                        {
                            case AT_PNT_HOME:
                            {
                                p_uni->n_location[0] = (Uint16Type)AT_LOC_HOME;
                                break;
                            }
                            case AT_PNT_OFFICE:
                            {
                                p_uni->n_location[0] = (Uint16Type)AT_LOC_OFFICE;
                                break;
                            }
                            case AT_PNT_MOBILE:
                            {
                                p_uni->n_location[0] = (Uint16Type)AT_LOC_MOBILE;
                                break;
                            }
                            case AT_PNT_OTHER:
                            {
                                p_uni->n_location[0] = (Uint16Type)AT_LOC_OTHER;
                                break;
                            }
                            case AT_PNT_GENERAL:
                            default:
                            {
                                p_uni->n_location[0] = (Uint16Type)AT_LOC_OTHER;
                                break;
                            }
                        }

                        /* RT#4940 do the following entry also PPBR-AT command
                           check if the location received map the one given in SIVR
                         * command - if so increment ncat */
                        if (BT_TRUE == p_uni->location_avail)
                        {
                            if (p_uni->location_id ==
                                p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1] )
                            {
                                p_uni->n_cat =  p_uni->n_cat + 1;
                            }
                        }

                    }
                    else
                    {   /* we have more than one entry - use ppbp */
                        for (i=0; (i< MAX_REQUESTED_PB_NUMBERS_PER_ENTRY &&
                                   i < VCR_MAXNUM_RES_STORE ); i++)
                        {
                            if (BT_TRUE == p_pb_entry->number[i].isValid)
                            {
                                /* check that required item id match */
                                if (p_uni->item_id == p_pb_entry->number[i].ppbp.pb_idx)
                                {   /* fill received location in table with received index -1 */
                                    switch (p_pb_entry->number[i].ppbp.pn_type)
                                    {
                                        case AT_PNT_HOME:
                                        {
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u] = (Uint16Type)AT_LOC_HOME;
                                            break;
                                        }
                                        case AT_PNT_OFFICE:
                                        {
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u] = (Uint16Type)AT_LOC_OFFICE;
                                            break;
                                        }
                                        case AT_PNT_MOBILE:
                                        {
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u] =(Uint16Type) AT_LOC_MOBILE;
                                            break;
                                        }
                                        case AT_PNT_OTHER:
                                        {
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u] = (Uint16Type)AT_LOC_OTHER;
                                            break;
                                        }
                                        case AT_PNT_GENERAL:
                                        default:
                                        {
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u] = (Uint16Type)AT_LOC_OTHER;
                                            break;
                                        }
                                    }
                                    /* OR available loc to have all available locs in one
                                     * variable */
                                    p_uni->avail_loc = p_uni->avail_loc |
                                                       p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1u];
                                    /* check if the location received map the one given in SIVR
                                     * command - if so increment ncat */
                                    if (BT_TRUE == p_uni->location_avail)
                                    {
                                        if (p_uni->location_id ==
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1] )
                                        {
                                            p_uni->n_cat =  p_uni->n_cat + 1;
                                        }
                                    }
                                    /* check if we have more than one available category */
                                    if (1 == p_pb_entry->number[i].ppbp.pn_idx)
                                    {
                                        _VCR_gData.resultInfo.contact_uni.c = 1u;
                                    }
                                    else
                                    {   /* compare category of this with first one, if differ we havbe more
                                           than one category */
                                        if (p_uni->n_location[0] !=
                                            p_uni->n_location[p_pb_entry->number[i].ppbp.pn_idx-1])
                                        {
                                            _VCR_gData.resultInfo.contact_uni.c = 2u;
                                        }
                                    }
                                    /* store number information */
                                    (void)memset(&(p_uni->phone_number[i]),0x00,sizeof(ATPhoneNumberType));
                                    if (0 < p_pb_entry->number[i].ppbp.phone_number_len)
                                    {
                                        p_uni->phone_number_len[i] =
                                            p_pb_entry->number[i].ppbp.phone_number_len;
                                        (void)memcpy(&(p_uni->phone_number[i]),
                                               &(p_pb_entry->number[i].ppbp.phone_number),
                                               p_uni->phone_number_len[i]);
                                    }
                                }
                            }
                        }

                    }

                    DEBUG_VALUE2(VCR_SWI_TRC, MOD_VCR, "pbonbook idx %d phone numbers %d", _VCR_gData.resultInfo.contact_uni.item_id, _VCR_gData.resultInfo.contact_uni.n);
                    DEBUG_VALUE2(VCR_SWI_TRC, MOD_VCR, "pbonbook idx %d location %d", _VCR_gData.resultInfo.contact_uni.item_id,
                                                                                      _VCR_gData.resultInfo.contact_uni.n_location[0]);

                    /* trigger state machine */
                    (void)_VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_PBENTRY_DATA_AVAIL,
                            AT_SIVR_CMD_UNKNOWN);
                }
                else
                {
                    /* an error occured - stop machine with error -
                       TODO error code to be added */
                    _VCR_PlayError(__LINE__);
                }
            }
            else
            {
                /* an error occured - stop machine with error -
                   TODO error code to be added */
                _VCR_PlayError(__LINE__);
            }
            break;
        }
        case APP_COMMON_RSP_ERROR_BUSY:
        case APP_COMMON_RSP_ERROR:
        case APP_COMMON_RSP_ERROR_BAP_PARSE:
        case APP_COMMON_RSP_ERROR_UART_BLOCKED:
        case APP_COMMON_RSP_ERROR_AT_SEND:
        case APP_COMMON_RSP_ERROR_AT_BUFFER_TOO_SHORT:
        case APP_COMMON_RSP_ERROR_NOT_INITIALIZED:
        default:
        {   /* an error occured - stop machine with error -
               TODO error code to be added */
            _VCR_PlayError(__LINE__);
            break;
        }
    }
}

