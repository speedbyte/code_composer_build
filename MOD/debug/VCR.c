/* Function    : VCR_rcPlayTestPrompt                                         */
/**
    \brief      lay test prompt for diagnose

    \param      callback

    \return     APP_COMMON_enResponseCode
                BUSY if SIVR is current busy
                ERROR if SIVR is unavailable
                APP_COMMON_RSP_OK if prompt will be started now
   */
/*----------------------------------------------------------------------------*/
APP_COMMON_enResponseCode VCR_rcPlayTestPrompt(
        void (*cb)(APP_COMMON_enResponseCode))
{
    APP_COMMON_enResponseCode retVal = APP_COMMON_RSP_OK;

    if ((BT_TRUE == APP_PHONE_AT_PNCC_bAllCallStatesIdle()) &&
        (ERM_SIVR_NOT_SUPPORTED != _VCR_gData.eSIVRSupported) &&               /* RT 2429 */
        (BT_FALSE == _VCR_gData.bSivrSyncStarted))
    {
        switch (_VCR_gData.eSystemState)
        {
            case VCR_SYSTEM_NOT_AVAILABLE:
            {   /* do nothing */
                retVal = APP_COMMON_RSP_ERROR;
                DEBUG_TEXT(VCR_SWI_WNG, MOD_VCR,"VCR_rcPlayTestPrompt - VR is not ready -> ignore testpromt");
                break;
            }
            case VCR_SYSTEM_AVAILABLE:
            {   /* system not started -
                   start system for the prompt only - usage similar to haptic */
                DEBUG_TEXT(VCR_SWI_TRC, MOD_VCR,"VCR_rcPlayTestPrompt - start VR -> play testpromt");
                _VCR_gData.eHapticTrigType = VCR_HAPTIC_PLAY_TEST;
                _VCR_gData.u16HapticIdx    = 0;
                (void)_VCR_sFHandleEvent(BT_TRUE,VCR_MEVT_HAPTIC,AT_SIVR_CMD_UNKNOWN);
                break;
            }
            default:
            {   /* mark indication that BT con prompt needs to be send  */
                retVal = APP_COMMON_RSP_ERROR_BUSY;
                DEBUG_VALUE1(VCR_SWI_TRC, MOD_VCR,"VCR_rcPlayTestPrompt - VR is busy SystemState %d", _VCR_gData.eSystemState);
                break;
            }
        }
    }
    else
    {

        DEBUG_VALUE2(VCR_SWI_TRC, MOD_VCR,"VCR_rcPlayTestPrompt - VR is busy SystemState %d or Sync %d", _VCR_gData.eSystemState,
                                                                                                         _VCR_gData.bSivrSyncStarted);
        retVal = APP_COMMON_RSP_ERROR_BUSY;
    }

    return retVal;
}

