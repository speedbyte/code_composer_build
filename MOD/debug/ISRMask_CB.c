/* Function    : ApplInterruptFlagMask                                        */
/** 
    \brief      Callback function for set interrupt bits
  
    \brief      Description:\n
                function to set bits for the diferent interrupts \n
                
    \return     void
   */
/*----------------------------------------------------------------------------*/
void ApplInterruptFlagMask(void)
{
    *((volatile uint32*)INT_REQMASK) &= ~INTERRUPT_FLAGS;   /* disable interrupt */
    *((volatile uint32*)INT_FIRQPR)  &= ~INTERRUPT_FLAGS;   /* interrupt is IRQ */
    *((volatile uint32*)INT_INTREQ)  &= ~INTERRUPT_FLAGS;   /* clear pending flag */
    *((volatile uint32*)INT_REQMASK) |= INTERRUPT_FLAGS;    /* enable interrupt  */ 
}/* END OF ApplInterruptFlagMask() */


